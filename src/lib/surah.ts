/**
 * Get the surah name (Arabic) for a given Mus'haf page (1–604).
 * Based on number of pages read or the page the user is currently on.
 */
export function getSurahNameForPage(page: number): string {
  if (page < 1) return SURAH_NAMES[0]!;
  if (page > 604) return SURAH_NAMES[113]!;
  let i = 0;
  for (let s = 0; s < SURAH_START_PAGE.length; s++) {
    if (page >= SURAH_START_PAGE[s]!) i = s;
  }
  return SURAH_NAMES[i] ?? SURAH_NAMES[0]!;
}
